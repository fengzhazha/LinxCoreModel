static void EncodeBlock64_extract_LoadSymbol(InstInfo *inst, uint64_t &bin)
{
    // inst->src2 = MInst::Deposit(MInst::Extract64(bin, 47, 17), 17, MInst::Extract64(bin, 7, 25));
    bin = MInst::Deposit64(bin, 47, 17, inst->src2);
    bin = MInst::Deposit64(bin, 7, 25, inst->src2 >> 17);
    // inst->dst0 = MInst::Extract64(bin, 39, 5);
    bin = MInst::Deposit64(bin, 39, 5, inst->dst0);
}
static void EncodeBlock64_extract_LongImmediate(InstInfo *inst, uint64_t &bin)
{
    // inst->src0 = MInst::Extract64(bin, 47, 5);
    bin = MInst::Deposit64(bin, 47, 5, inst->src0);
    // inst->src2 = MInst::Deposit(MInst::Extract64(bin, 52, 12), 12, MInst::Extract64(bin, 12, 20));
    bin = MInst::Deposit64(bin, 52, 12, inst->src2);
    bin = MInst::Deposit64(bin, 12, 20, inst->src2 >> 12);
    // inst->dst0 = MInst::Extract64(bin, 39, 5);
    bin = MInst::Deposit64(bin, 39, 5, inst->dst0);
}
static void EncodeBlock64_extract_StoreSymbol(InstInfo *inst, uint64_t &bin)
{
    // inst->src0 = MInst::Extract64(bin, 47, 5);
    bin = MInst::Deposit64(bin, 47, 5, inst->src0);
    inst->src2 = MInst::Deposit(MInst::Deposit(MInst::Extract64(bin, 52, 12), 12, MInst::Extract64(bin, 39, 5)), 17, MInst::Sextract64(bin, 7, 25));
}
